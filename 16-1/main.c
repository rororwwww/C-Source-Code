#include <stdio.h>

int main()
{
    printf("3<<2=%d\n",3<<2);
    printf("-8<<2=%d\n",-8<<2);
    // 3==> 11<<2 ==> 1100==>12
    // -8==>1111 1000<<2 ==>1110 0000==>-32

    //左移 高位舍弃低位补0


    printf("3>>2=%d\n",3>>2);
    printf("-8>>2=%d\n",-8>>2);
    // -8==>1111 1000>>2 ==>1111 1110==> -2
    // 右移 低位舍弃 高位补符号位

    //有符号数在内存中以补码形式存储
    //正数 补码等于原码 负数补码等于反码+1
    //原码转换补码 取反+1
    //补码转换原码 -1取反

    return 0;
}
